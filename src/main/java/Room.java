import java.util.ArrayList;

public class Room {
//    Data Members
private int width;
private int length;
private int height;
private int numWindows;
//    Constructors

    public Room(int width, int length, int height, int numWindows) {
        this.width = width;
        this.length = length;
        this.height = height;
        this.numWindows = numWindows;

    }
    //    Accessors

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getNumWindows() {
        return numWindows;
    }

    public void setNumWindows(int numWindows) {
        this.numWindows = numWindows;
    }


//    Methods










}
